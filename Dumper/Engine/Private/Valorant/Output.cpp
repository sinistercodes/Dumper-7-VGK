        // ------------------------------------------------------------------ //
        // Usage example
        // ------------------------------------------------------------------ //
        out << "/*\n";
        out << " * Usage example -- consumer flow against the live game.\n";
        out << " * Pseudocode; does not need to compile as-is.\n";
        out << " *\n";
        out << " *   uintptr_t base = GetModuleBase(\"VALORANT-Win64-Shipping.exe\");\n";
        out << " *\n";
        out << " *   // 1. Decrypt GObjects to get the FUObjectArray base.\n";
        out << " *   uintptr_t fuoa = (uintptr_t)decrypt_gobjects(base);\n";
        out << " *\n";
        out << " *   // 2. Walk GObjects to find a non-default UEngine instance. The\n";
        out << " *   //    static GWorld slot is zeroed in Valorant, so the supported path\n";
        out << " *   //    is UEngine -> GameViewport -> World. Cache `engine` after the\n";
        out << " *   //    first hit -- it does not move for the process lifetime.\n";
        out << " *   uintptr_t chunks = ReadPtr(fuoa + 0x00);\n";
        out << " *   int32_t   num    = ReadI32(fuoa + 0x14);\n";
        out << " *   uintptr_t engine = 0;\n";
        out << " *   for (int32_t i = 0; i < num && !engine; ++i) {\n";
        out << " *       uintptr_t chunk = ReadPtr(chunks + (i >> 16) * 8);\n";
        out << " *       uintptr_t obj   = ReadPtr(chunk  + (i & 0xFFFF) * 0x18);\n";
        out << " *       // FUObjectItem stride = 0x18; Object* lives at +0.\n";
        out << " *       if (obj && IsUEngineSubclassNonDefault(obj)) engine = obj;\n";
        out << " *   }\n";
        out << " *\n";
        out << " *   // 3. UEngine -> GameViewport -> World.\n";
        out << " *   uintptr_t viewport = ReadPtr(engine   + UEOffsets::UEngine_GameViewport);\n";
        out << " *   uintptr_t world    = ReadPtr(viewport + UEOffsets::UGameViewportClient_World);\n";
        out << " *\n";
        out << " *   // 4. Recover the FName key once (stable per session).\n";
        out << " *   uintptr_t pool      = ReadPtr(base + FNamePool);\n";
        out << " *   uint32_t  fname_key = recover_fname_key(pool);\n";
        out << " *\n";
        out << " *   // 5. Walk actors and resolve their names.\n";
